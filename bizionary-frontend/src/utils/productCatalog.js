export const getCategoryCatalog = (category) => PRODUCT_CATALOG.find((item) => item.value === category) || null;

export const getSubcategoriesForCategory = (category) => getCategoryCatalog(category)?.subcategories || [];

export const getProductsForCategoryAndSubcategory = (category, subcategory) => {
    const catalog = getCategoryCatalog(category);
    if (!catalog) {
        return [];
    }

    const group = catalog.subcategories.find((item) => item.value === subcategory);
    return group?.products || [];
};

export const findCatalogProduct = (category, subcategory, productName) => {
    const products = getProductsForCategoryAndSubcategory(category, subcategory);
    return products.find((item) => item === productName) || '';
};